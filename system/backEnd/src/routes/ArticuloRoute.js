router.put("/activate", auth.verifyStorer, CtrlArticulo.activate);
router.put("/deactivate", auth.verifyStorer, CtrlArticulo.deactivate);
router.delete("/remove", auth.verifyStorer, CtrlArticulo.remove);
module.exports = router;
