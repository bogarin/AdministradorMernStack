import routex from "express-promise-router";
import CtrlArticulo from "../controllers/ArticuloController";
import auth from "../middleware/auth";
const router = routex();
router.post("/add", auth.verifyStorer, CtrlArticulo.add);
router.get("/query", auth.verifyStorer, CtrlArticulo.query);
router.get("/queryCodigo", auth.verifyUser, CtrlArticulo.queryCodigo);
